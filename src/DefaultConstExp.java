public class DefaultConstExp {
    int a;
    float b;

    boolean c;

    public static void main(String[] args) {
        DefaultConstExp d=new DefaultConstExp();
        //d.a=34;
        //d.b=78.9f;
        System.out.println("a value =  "+d.a);
        System.out.println("b value =  "+d.b);
        System.out.println("c value =  "+d.c);
    }

}
